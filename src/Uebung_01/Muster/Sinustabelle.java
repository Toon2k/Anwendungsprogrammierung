package Uebung_01.Muster;

import static java.lang.Math.*; // Statischer Import

public class  Sinustabelle {

  public static void main(String [] argumente)
  {
  double x, h, g;
    
  //System.out.println("vorrechnung: " + menge);
    System.out.println("   x =                sin(x) =");
    System.out.println("----------------------------------------");  
    h = PI / 20; //Schrittweite
    g = 2 * PI; //Endbedingung
    for (x = 0.0; x <= g ; x = x + h)  
      System.out.println(x + " = " + sin(x));
   // Problematisch: Einfacher Vergleich von Gleitpunktzahlen auf Gleichheit
// Hier korrekter Ablauf, aber siehe Beispiele für fehlerhafte Durchführung
// (ProblemeMitVergleichGleitkommazahlen.java)

 System.out.println();
System.out.println("Alternative:");
 System.out.println("Option 2:");
    System.out.println("   x =                sin(x) =");
    System.out.println("----------------------------------------");  
    h = PI / 20; //Schrittweite
    double menge = 2 * PI / (PI / 20); // Menge an Schritten
    for (int i = 0; i <= menge; i++) {
      x = i * h;      	
      System.out.println(x + " = " + sin(x));
    }
    
    //Erweiterung mit schoener Formartierung
    System.out.println();
    System.out.println("Alternative schoen formartiert:");
     System.out.format("   x =            sin(x) =%n");
    System.out.format("-----------------------------%n");  
    h = Math.PI / 20;
    for (int i = 0; i <= 40; i++) {
      x = i * h;
      System.out.format(" %1.4f % 1.4f%n", x, Math.sin(x));
    }

      
  }                                             

}

